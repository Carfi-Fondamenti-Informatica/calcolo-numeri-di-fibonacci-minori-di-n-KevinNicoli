#include <iostream>
using namespace std;

int main() {
    int n=0, m=0 , l=0, j=1, k=0, i=0;
    cin >> n;
    cout << j << endl;
    while (k <= n) {
            k = j + l;
            m= k - l;
            cout << m << endl;
            i=j;
            j= i+l;
            l=i;
    }
    return 0;
}
